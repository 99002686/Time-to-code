/**
* Check whether the integer number prime
* @param number for which  prime number check has to be found
* @return whether the number is a prime number
* @note Returns 1 for prime number
* @note Returns 0 for composite number
* @note Returns -1 for 1/0 for neither prime nor composite numbers 
*/
int prime(int number);

